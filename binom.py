def binomial_coefficient(n, k):
    # Инициализируем таблицу значений
    table = [[0 for x in range(k+1)] for x in range(n+1)]

    # Заполняем первую строку таблицы нулями
    for i in range(k+1):
        table[0][i] = 0

    # Заполняем первый столбец таблицы единицами
    for i in range(n+1):
        table[i][0] = 1

    # Заполняем оставшиеся ячейки таблицы
    for i in range(1, n+1):
        for j in range(1, k+1):
            # Если j > i, то C(i,j) равен нулю
            if j > i:
                table[i][j] = 0
            # Если j <= i, то C(i,j) равен сумме значений C(i-1,j-1) и C(i-1,j)
            else:
                table[i][j] = table[i-1][j-1] + table[i-1][j]

    # Возвращаем значение C(n,k)
    return table[n][k]

print(binomial_coefficient(50, 20))

# Функция binomial_coefficient принимает два аргумента - целые числа n и k, и возвращает значение биномиального коэффициента C(n,k).

# Для вычисления значения C(n,k) программа использует таблицу значений размером (n+1) x (k+1), которая заполняется построчно с помощью циклов. 
# В первой строке таблицы заполняются нули, а в первом столбце - единицы. 
# Затем оставшиеся ячейки таблицы заполняются с помощью формулы C(i,j) = C(i-1,j-1) + C(i-1,j), если j <= i, и C(i,j) = 0, если j > i.

# В конце функция возвращает значение C(n,k), которое находится в последней ячейке таблицы.